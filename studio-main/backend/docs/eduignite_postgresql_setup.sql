-- Run this block connected to the 'postgres' database as superuser.
-- ─────────────────────────────────────────────────────────────────────────────

-- 1.1  Create application role
DO $$
BEGIN
  IF NOT EXISTS (SELECT 1 FROM pg_roles WHERE rolname = 'eduignite_user') THEN
    CREATE ROLE eduignite_user
      LOGIN
      PASSWORD 'EduIgnite_DB_2026!'
      NOSUPERUSER
      NOCREATEDB
      NOCREATEROLE;
    RAISE NOTICE 'Role eduignite_user created.';
  ELSE
    RAISE NOTICE 'Role eduignite_user already exists – skipping.';
  END IF;
END
$$;

-- 1.2  Create database
DO $$
BEGIN
  IF NOT EXISTS (SELECT 1 FROM pg_database WHERE datname = 'eduignite') THEN
    PERFORM dblink_exec('dbname=postgres', 'CREATE DATABASE eduignite
      OWNER = eduignite_user
      ENCODING = ''UTF8''
      LC_COLLATE = ''en_US.UTF-8''
      LC_CTYPE   = ''en_US.UTF-8''
      TEMPLATE = template0');
    RAISE NOTICE 'Database eduignite created.';
  ELSE
    RAISE NOTICE 'Database eduignite already exists – skipping.';
  END IF;
END
$$;

