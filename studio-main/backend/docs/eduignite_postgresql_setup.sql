-- 8.1  Find students with no parent link
SELECT
    u.matricule, u.name, u.school_id
FROM users u
JOIN students_student st ON st.user_id = u.id
WHERE u.role = 'STUDENT'
  AND NOT EXISTS (
      SELECT 1 FROM students_parentstudentlink l WHERE l.student_id = st.id
  );

-- 8.2  Find schools missing settings
SELECT s.id, s.name
FROM schools s
WHERE NOT EXISTS (
    SELECT 1 FROM school_settings ss WHERE ss.school_id = s.id
);

-- 8.3  Find teachers assigned to no subjects
SELECT u.matricule, u.name, u.school_id
FROM users u
WHERE u.role = 'TEACHER'
  AND NOT EXISTS (
      SELECT 1 FROM grades_subject gs WHERE gs.teacher_id = u.id AND gs.is_active = TRUE
  );

-- 8.4  Find overdue loans not yet marked (useful for manual audits)
SELECT
    bl.id,
    u.name      AS borrower,
    b.title     AS book,
    bl.due_date,
    CURRENT_DATE - bl.due_date AS days_overdue
FROM library_bookloan bl
JOIN library_book  b  ON b.id  = bl.book_id
JOIN users         u  ON u.id  = bl.borrower_id
WHERE bl.status   = 'Active'
  AND bl.due_date < CURRENT_DATE
ORDER BY days_overdue DESC;

-- 8.5  Unread announcements count per user (sample – top 20)
SELECT
    u.name, u.role, u.school_id,
    COUNT(a.id) - COUNT(ar.id) AS unread_count
FROM users u
JOIN announcements_announcement a
    ON (a.school_id = u.school_id OR a.school_id IS NULL)
    AND a.target IN ('ALL', 'SCHOOL_ALL', u.role)
    AND (a.expires_at IS NULL OR a.expires_at > NOW())
LEFT JOIN announcements_announcementread ar
    ON ar.announcement_id = a.id AND ar.user_id = u.id
WHERE u.is_active = TRUE
GROUP BY u.id, u.name, u.role, u.school_id
ORDER BY unread_count DESC
LIMIT 20;

