-- 5.3  Auto-update book available_copies when a loan status changes
CREATE OR REPLACE FUNCTION fn_sync_book_available_copies()
RETURNS TRIGGER LANGUAGE plpgsql AS $$
BEGIN
  IF TG_OP = 'INSERT' AND NEW.status = 'Active' THEN
    UPDATE library_book
    SET available_copies = GREATEST(available_copies - 1, 0)
    WHERE id = NEW.book_id;

  ELSIF TG_OP = 'UPDATE' THEN
    IF OLD.status = 'Active' AND NEW.status IN ('Returned', 'Lost') THEN
      UPDATE library_book
      SET available_copies = LEAST(available_copies + 1, total_copies)
      WHERE id = NEW.book_id AND NEW.status = 'Returned';
    END IF;
  END IF;
  RETURN NEW;
END;
$$;

DROP TRIGGER IF EXISTS trg_book_copies ON library_bookloan;
CREATE TRIGGER trg_book_copies
AFTER INSERT OR UPDATE OF status ON library_bookloan
FOR EACH ROW
EXECUTE FUNCTION fn_sync_book_available_copies();

-- 5.4  Auto-update last_message on conversation when a new message is inserted
CREATE OR REPLACE FUNCTION fn_update_conversation_last_message()
RETURNS TRIGGER LANGUAGE plpgsql AS $$
BEGIN
  UPDATE chat_conversation
  SET
    last_message    = LEFT(NEW.text, 100),
    last_message_at = NEW.created_at
  WHERE id = NEW.conversation_id;
  RETURN NEW;
END;
$$;

DROP TRIGGER IF EXISTS trg_convo_last_msg ON chat_message;
CREATE TRIGGER trg_convo_last_msg
AFTER INSERT ON chat_message
FOR EACH ROW
WHEN (NEW.is_deleted = FALSE AND NEW.text IS NOT NULL)
EXECUTE FUNCTION fn_update_conversation_last_message();

-- 5.5  Prevent duplicate active sequences per school/term
CREATE OR REPLACE FUNCTION fn_enforce_single_active_sequence()
RETURNS TRIGGER LANGUAGE plpgsql AS $$
BEGIN
  IF NEW.is_active = TRUE THEN
    UPDATE grades_sequence
    SET is_active = FALSE
    WHERE school_id = NEW.school_id
      AND academic_year = NEW.academic_year
      AND id != NEW.id;
  END IF;
  RETURN NEW;
END;
$$;

DROP TRIGGER IF EXISTS trg_single_active_sequence ON grades_sequence;
CREATE TRIGGER trg_single_active_sequence
BEFORE INSERT OR UPDATE OF is_active ON grades_sequence
FOR EACH ROW
WHEN (NEW.is_active = TRUE)
EXECUTE FUNCTION fn_enforce_single_active_sequence();

