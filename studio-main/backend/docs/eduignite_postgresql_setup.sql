-- 7.1  Compute and persist term ranks for a school/year/term
CREATE OR REPLACE PROCEDURE sp_compute_term_ranks(
    p_school_id   VARCHAR,
    p_academic_year VARCHAR,
    p_term        INTEGER
)
LANGUAGE plpgsql AS $$
BEGIN
  WITH ranked AS (
    SELECT
      id,
      ROW_NUMBER() OVER (
        PARTITION BY school_id, academic_year, term
        ORDER BY average DESC
      ) AS new_rank,
      COUNT(*) OVER (
        PARTITION BY school_id, academic_year, term
      ) AS total
    FROM grades_termresult
    WHERE school_id     = p_school_id
      AND academic_year = p_academic_year
      AND term          = p_term
  )
  UPDATE grades_termresult tr
  SET rank = r.new_rank, total_students = r.total
  FROM ranked r
  WHERE tr.id = r.id;

  RAISE NOTICE 'Term ranks updated for school=% year=% term=%',
    p_school_id, p_academic_year, p_term;
END;
$$;

-- 7.2  Mark all overdue loans for a school
CREATE OR REPLACE PROCEDURE sp_mark_overdue_loans(p_school_id VARCHAR)
LANGUAGE plpgsql AS $$
BEGIN
  UPDATE library_bookloan
  SET status = 'Overdue'
  WHERE school_id   = p_school_id
    AND status      = 'Active'
    AND due_date    < CURRENT_DATE;

  RAISE NOTICE 'Overdue loans updated for school=%', p_school_id;
END;
$$;

-- 7.3  Get full user profile with role-specific data
CREATE OR REPLACE FUNCTION fn_get_user_profile(p_user_id UUID)
RETURNS JSON LANGUAGE plpgsql AS $$
DECLARE
  v_user    RECORD;
  v_student RECORD;
  v_result  JSON;
BEGIN
  SELECT id, name, email, matricule, role, school_id, phone,
         is_active, is_license_paid, date_joined, last_login
  INTO v_user FROM users WHERE id = p_user_id;

  IF NOT FOUND THEN
    RETURN json_build_object('error', 'User not found');
  END IF;

  IF v_user.role = 'STUDENT' THEN
    SELECT student_class, class_level, section, admission_number, annual_average, is_on_honour_roll
    INTO v_student FROM students_student WHERE user_id = p_user_id;

    v_result := json_build_object(
      'id',              v_user.id,
      'name',            v_user.name,
      'email',           v_user.email,
      'matricule',       v_user.matricule,
      'role',            v_user.role,
      'school_id',       v_user.school_id,
      'class',           v_student.student_class,
      'class_level',     v_student.class_level,
      'annual_average',  v_student.annual_average,
      'honour_roll',     v_student.is_on_honour_roll
    );
  ELSE
    v_result := row_to_json(v_user);
  END IF;

  RETURN v_result;
END;
$$;

-- 7.4  Send school-wide announcement (inserts record)
CREATE OR REPLACE PROCEDURE sp_create_announcement(
    p_school_id VARCHAR,
    p_sender_id UUID,
    p_title     TEXT,
    p_content   TEXT,
    p_target    VARCHAR DEFAULT 'SCHOOL_ALL',
