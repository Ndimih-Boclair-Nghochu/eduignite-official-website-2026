-- 5.1  Auto-update school student count when a student user is created/deleted
CREATE OR REPLACE FUNCTION fn_update_school_student_count()
RETURNS TRIGGER LANGUAGE plpgsql AS $$
BEGIN
  IF TG_OP = 'INSERT' THEN
    UPDATE schools SET student_count = (
      SELECT COUNT(*) FROM users WHERE school_id = NEW.school_id AND role = 'STUDENT'
    ) WHERE id = NEW.school_id;
  ELSIF TG_OP = 'DELETE' THEN
    UPDATE schools SET student_count = (
      SELECT COUNT(*) FROM users WHERE school_id = OLD.school_id AND role = 'STUDENT'
    ) WHERE id = OLD.school_id;
  ELSIF TG_OP = 'UPDATE' AND OLD.school_id IS DISTINCT FROM NEW.school_id THEN
    UPDATE schools SET student_count = (
      SELECT COUNT(*) FROM users WHERE school_id = OLD.school_id AND role = 'STUDENT'
    ) WHERE id = OLD.school_id;
    UPDATE schools SET student_count = (
      SELECT COUNT(*) FROM users WHERE school_id = NEW.school_id AND role = 'STUDENT'
    ) WHERE id = NEW.school_id;
  END IF;
  RETURN NEW;
END;
$$;

DROP TRIGGER IF EXISTS trg_student_count ON users;
CREATE TRIGGER trg_student_count
AFTER INSERT OR UPDATE OF school_id, role OR DELETE ON users
FOR EACH ROW
WHEN (
  (TG_OP = 'INSERT'  AND NEW.role = 'STUDENT') OR
  (TG_OP = 'DELETE'  AND OLD.role = 'STUDENT') OR
  (TG_OP = 'UPDATE'  AND (NEW.role = 'STUDENT' OR OLD.role = 'STUDENT'))
)
EXECUTE FUNCTION fn_update_school_student_count();

